package com.operators;

public class Day7 {

	public static void main(String[] args) {

		// Assignment Operator

		int a = 37;

		System.out.println(a += 7); // 44
		System.out.println(a -= 4); // 40 
		System.out.println(a *= 2); // 80
		System.out.println(a /= 6); // 13
		System.out.println(a %= 8); // 5

	}
}
