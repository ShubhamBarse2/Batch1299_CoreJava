package com.operators;

public class Practise1 {

	public static void main(String[] args) {
		int i = 23;

		i++; // 24

		System.out.println(i); // 24
		i--; // 23
		System.out.println(i+=2); // 23 +2 = 25

		System.out.println(i++); // 26
		i--; // 25
		System.out.println(i--); // 24
		i++; // 25
		i--; // 24
		System.out.println(i); // 24
	}

}
